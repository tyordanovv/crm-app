package eu.elex8x.notificationservice.model;

public enum ConditionOperator {
    EQUAL,              // Equal (==)
    NOT_EQUAL,          // Not Equal (!=)
    GREATER_THAN,       // Greater Than (>)
    LESS_THAN,          // Less Than (<)
    GREATER_OR_EQUAL,   // Greater Than or Equal To (>=)
    LESS_OR_EQUAL       //Less Than or Equal To (<=)
}
